package cs454.webCrawler.webCrawler;

public class Document
{
	int no;
	int count;
	
	public Document()
	{
		
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
}
